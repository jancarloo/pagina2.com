if (edad > 20) {
    document.write("Tienes más de 20 años");
}else{
    document.write("estas muy peque niña");
}

    

for (let i = 0; i = edad; i++) {

    for (let a = 0; a = edad; a++) {       
        let result = --edad;
        document.write(result);
    }
}